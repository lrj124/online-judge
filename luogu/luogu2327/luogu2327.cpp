#include <bits/stdc++.h>
using namespace std;
const int maxn = 10000 + 10;
int n,a[maxn],f[maxn][2][2][2];
int main() {
	freopen("luogu2327.in","r",stdin);
	freopen("luogu2327.out","w",stdout);
	scanf("%d",&n);
	for (int i = 1;i <= n;i++) scanf("%d",&a[i]);
	if (a[1] == 0) f[1][0][0][0] = 1;
	if (a[1] == 1) f[1][0][0][1] = f[1][0][1][0] = 1;
	if (a[1] == 2) f[1][0][1][1] = 1;
	if (a[1] >= 3) return printf("0"),0;
	for (int i = 2;i <= n;i++) {
		if (a[i] == 0) f[i][0][0][0] = f[i-1][0][0][0]+f[i-1][1][0][0];
		if (a[i] == 1) {
			f[i][0][0][1] += f[i-1][0][0][0]+f[i-1][1][0][0];
			f[i][0][1][0] += f[i-1][0][0][1]+f[i-1][1][0][1];
			f[i][1][0][0] += f[i-1][0][1][0]+f[i-1][1][1][0];
		}
		if (a[i] == 2) {
			f[i][0][1][1] += f[i-1][0][0][1]+f[i-1][1][0][1];
			f[i][1][1][0] += f[i-1][0][1][1]+f[i-1][1][1][1];
			f[i][1][0][1] += f[i-1][0][1][0]+f[i-1][1][1][0];
		}
		if (a[i] == 3) f[i][1][1][1] += f[i-1][0][1][1]+f[i-1][1][1][1];
	}
	printf("%d",f[n][0][0][0]+f[n][1][0][0]+f[n][0][1][0]+f[n][1][1][0]);
	return 0;
}
